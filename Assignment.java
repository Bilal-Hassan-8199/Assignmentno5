// Java code to illustrate "+=" 

import java.io.*; 

class Assignment { 
	public static void main(String[] args) 
	{ 

		// Declaring variables 
		int a = 1, b = 2; 

		System.out.println("a = " + a); 
		System.out.println("b = " + b); 

		// Adding & Assigning values 
		a += b; 

		// Displaying the assigned values 
		System.out.println("b = " + a); 
	} 
} 
